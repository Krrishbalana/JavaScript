//------------------------------------------------------Loops----------------------------------------------------------------
// loops are used to execute a piece of code again and again

//1.for loop
for(let i = 1; i <= 5; i++){
    console.log(i ,"krish ");
}

//2.While loop
let i = 1;
while(i <= 5){
 console.log(i);
 i++;
}

//3.Do-While loop --> even the condition is false it will run for first time
let a = 1;
do{
    console.log(a);
    a++;
}while(a < 0);
